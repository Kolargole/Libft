#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;

	i = 0;
	while (((unsigned char *)b) + i && i < len)
	{
		((unsigned char *)b)[i] = c;
		i++;
	}
	return (b);
}
